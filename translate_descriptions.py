def main():
    conn = sqlite3.connect(DB)
    cur = conn.cursor()
    
    # Verify courses exist
    cur.execute("""
        SELECT id, name, description FROM skill_courses 
        WHERE published = 1 
        AND description NOT LIKE '%的%' 
        AND description NOT LIKE '%学%' 
        AND description NOT LIKE '%能%'
    """)
    rows = cur.fetchall()
    print(f"Found {len(rows)} courses with English descriptions")
    
    updated = 0
    missing = []
    for row in rows:
        cid, name, desc = row
        if cid in TRANSLATIONS:
            cur.execute("UPDATE skill_courses SET description = ? WHERE id = ?", (TRANSLATIONS[cid], cid))
            updated += 1
            print(f"  ✅ {name}")
        else:
            missing.append((cid, name))
            print(f"  ❌ Missing translation: {name} ({cid})")
    
    conn.commit()
    conn.close()
    print(f"\nDone: {updated} courses translated, {len(missing)} missing")

if __name__ == "__main__":
    main()
